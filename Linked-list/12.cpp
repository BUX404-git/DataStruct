/**
 * 在一个递增有序的线性表中,有数值相同的元素存在.
 * 若存储方式为单链表,设计算法去掉数值相同的元素,使表中不再有重复的元素,
 * 例如:(7,10,10,21,30,42,42,42,51,70)变为
 *      (7,10,21,30,42,51,70)
 * 
 * 思路:由于是有序链表,用p扫描链表,若*p结点的值域等于其后继结点的值域,则删除后者,
 *      否则移入下一个结点
 * 
 * */

LinkList Del_Same(LinkList &L){
	LNode *p=L->next,*q;//定义工作指针*p扫描链表,后继指针*q用来判断是否相等
	while(p->next != nullptr){
		q=p->next;
		if(p->data == q->data){//如果相等,则删除q结点
			p->next = q->next;
			free(q);
		}
		else
			p=p->next;//不相等则继续扫描
	}
	return L;
}

